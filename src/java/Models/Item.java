package Models;

/**
 *
 * @author FPT
 */
public class Item {
    private Book book;
    private int quantity;
    private float total;

    public Item() {
    }

    public Item(Book book, int quantity, float total) {
        this.book = book;
        this.quantity = quantity;
        this.total = total;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public float getTotal() {
        return total;
    }

    public void setTotal(float total) {
        this.total = total;
    }
    
    

}
